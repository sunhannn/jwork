
public class Opcalss {

	public static void main(String_0[] args) {
		int a =10;//선언+초기화한 형태
		int b = a;
		System.out.println(b);
		
		b += a;
		System.out.println(b);
		
		b -= a;
		System.out.println(b);
		
		b *= a;
		System.out.println(b);
		
		b /= a;
		System.out.println(b);

		b %= a;
		System.out.println(b);


		int c =10;
		int d =5;
		//비교연산자 : 값으로 true, flase를 반환
		System.out.println("c == d : "+ (c == d)); 
		System.out.println("c > d : "+ (c > d));
		System.out.println("c >= d : "+ (c >= d));
		System.out.println("c < d : "+ (c < d));
		System.out.println("c <= d : "+ (c <= d));
		System.out.println("c != d : "+ (c != d));
		
	
	}

}
